// Opacity gradient: top lines fade out, bottom lines fully opaque
function lineOpacity(i: number, total: number): number {
  const t = i / (total - 1);
  return Math.round((1 - t * 0.7) * 100) / 100;
}

export default function AsciiProfile(props: { src: string }) {
  return (
    <Wrapper>
      <AsciiLayer>
        {asciiBlockLines.map((line, i) => (
          <span
            key={i}
            style={{ opacity: lineOpacity(i, asciiBlockLines.length) }}
          >
            {line}
          </span>
        ))}
      </AsciiLayer>
      <ImageLayer>
        <img src={props.src} alt="Profile photo of Tobias Facello" />
      </ImageLayer>
    </Wrapper>
  );
}
